            @if ($this->enabled)
                @if ($showingConfirmation)
                    {{ __('Finalisez l\'activation de l\'authentification à deux facteurs.') }}
                @else
                    {{ __('Vous avez activé l\'authentification à deux facteurs.') }}
                @endif
            @else
                {{ __('Vous n\'avez pas activé l\'authentification à deux facteurs.') }}
            @endif
        </h3>

        <div class="mt-3 max-w-xl text-sm text-gray-600">
            <p>
                {{ __('Lorsque la double authentification, un jeton aléatoire sera créé pour une période limitée. Vous le retrouverez sous forme de code dans votre application Google Authenticator sur votre smartphone.') }}
            </p>
        </div>

        @if ($this->enabled)
            @if ($showingQrCode)
                <div class="mt-4 max-w-xl text-sm text-gray-600">
                    <p class="font-semibold">
                        @if ($showingConfirmation)
                            {{ __('Pour terminer d\'activer la double authentification, scannez le QR code avec votre application d\'authentification ou entrez la clé de configuration et reportez le code OTP généré.') }}
